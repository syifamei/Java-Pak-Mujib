public class Bab4no1 {

    public static void main(String[] args) {
       // Inisialisasi variabel
        int angka1 = 10;
        int angka2 = 3;

        // Operasi aritmatika
        int hasilPenjumlahan = angka1 + angka2;
        int hasilPengurangan = angka1 - angka2;
        int hasilPerkalian = angka1 * angka2;
        double hasilPembagian = (double) angka1 / angka2;

        // Menampilkan hasil
        System.out.println("a. Hasil Pertambahan A + B = " + hasilPenjumlahan);
        System.out.println("b. Hasil Pengurangan A - B = " + hasilPengurangan);
        System.out.println("c. Hasil Perkalian A * B = " + hasilPerkalian);
        System.out.println("d. Hasil Pembagian A / B = " + hasilPembagian);
    }
}
